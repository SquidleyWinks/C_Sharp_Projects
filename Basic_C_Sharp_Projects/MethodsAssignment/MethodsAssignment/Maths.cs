using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MethodsAssignment
{
    class Maths
    {
        public static int AddFive(int n)
        {
            n += 5;
            return n;
        }

        public static int MultFive(int n)
        {
            n *= 5;
            return n;
        }

        public static int MinusFive(int n)
        {
            n -= 5;
            return n;
        }
    }
}
